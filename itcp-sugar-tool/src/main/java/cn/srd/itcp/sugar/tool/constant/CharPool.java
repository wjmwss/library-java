package cn.srd.itcp.sugar.tool.constant;

/**
 * 字符常量池
 *
 * @author wjm
 * @since 2021/2/1 20:38
 */
public interface CharPool {

    /**
     * 空格符 {@code ' '}
     */
    char SPACE = ' ';

    /**
     * 制表符 {@code '\t'}
     */
    char TAB = '	';

    /**
     * 点 {@code '.'}
     */
    char DOT = '.';

    /**
     * 斜杠 {@code '/'}
     */
    char SLASH = '/';

    /**
     * 反斜杠 {@code '\\'}
     */
    char BACKSLASH = '\\';

    /**
     * 回车符 {@code '\r'}
     */
    char CR = '\r';

    /**
     * 换行符 {@code '\n'}
     */
    char LF = '\n';

    /**
     * 减号（连接符） {@code '-'}
     */
    char DASHED = '-';

    /**
     * 下划线 {@code '_'}
     */
    char UNDERLINE = '_';

    /**
     * 逗号 {@code ','}
     */
    char COMMA = ',';

    /**
     * 花括号（左） <code>'{'</code>
     */
    char DELIM_START = '{';

    /**
     * 花括号（右） <code>'}'</code>
     */
    char DELIM_END = '}';

    /**
     * 中括号（左） {@code '['}
     */
    char BRACKET_START = '[';

    /**
     * 中括号（右） {@code ']'}
     */
    char BRACKET_END = ']';

    /**
     * 双引号 {@code '"'}
     */
    char DOUBLE_QUOTES = '"';

    /**
     * 单引号 {@code '\''}
     */
    char SINGLE_QUOTE = '\'';

    /**
     * 与 {@code '&'}
     */
    char AMP = '&';

    /**
     * 冒号 {@code ':'}
     */
    char COLON = ':';

    /**
     * 艾特 {@code '@'}
     */
    char AT = '@';

}
